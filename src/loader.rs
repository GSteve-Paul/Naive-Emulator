use crate::memory::paddr_write;

pub fn naive_load() {
    paddr_write(
        0x8000_0000.into(),
        4,
        0b00000000000_10001_00000_00001_000010,
    );
    paddr_write(
        0x8000_0004.into(),
        4,
        0b00000000000_10010_00000_00010_000010,
    );
    paddr_write(
        0x8000_0008.into(),
        4,
        0b00000000001_00010_00001_00011_000001,
    );
    paddr_write(
        0x8000_000C.into(),
        4,
        0b00000000000_00000_00000_00000_000000,
    );
}
